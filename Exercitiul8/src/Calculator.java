public class Calculator {

    //Constructor default - folosit la instantierea obiectelor
    public Calculator() {
    }


    public int sum(int p1, int p2) {
        return p1 + p2;
    }

    public int diff(int p1, int p2) {
        return p1 - p2;
    }

    public int mult(int p1, int p2) {
        return p1 * p2;
    }

    public int div(int p1, int p2) {
        return p1 / p2;
    }

}
