public class CalculatorStiintific  extends Calculator{

    public CalculatorStiintific(){

    }

    public double exponentiation(int p1, int p2){
        return Math.pow(p1, p2);
    }

    public double sqrt(int p1){
        return Math.sqrt(p1);
    }

    public double log10(int p1){
        return Math.log10(p1);
    }

    public double log(int p1){
        return Math.log(p1);
    }
}
